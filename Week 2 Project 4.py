radius = float(input("Enter Radius of the sphere: "))
diameter = radius * 2
circumference = 2 * 3.14 * radius
surfaceArea = 4 * 3.14 * radius ** 2
volume = (4/3) * (3.14 * radius ** 3)
print("Diameter of the sphere is: ", diameter)
print("Circumference of the sphere is: ", circumference)
print("Surface area of the sphere is: ", surfaceArea)
print("volume of the sphere is: ",volume)
        
